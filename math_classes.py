import math


def add(first, second):
    return first + second


def sub(first, second):
    return first - second


def mul(first, second):
    return first * second


def div(first, second):
    return first // second


def cos(float_):
    return math.cos(float_)


def sin(float_):
    return math.sin(float_)


def tg(float_):
    return math.tan(float_)


def tan(float_):
    return math.tan(float_)


def ln(float_):
    return math.log(float_)


def log(base, float_):
    return math.log(float_, base)


def lg(float_):
    return math.log10(float_)


def log2(float_):
    return math.log2(float_)


def fact(float_):
    return math.factorial(float_)


def exp(float_):
    return math.exp(float_)
